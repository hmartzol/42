#include "libft.h"

void	*ft_memalloc(size_t size)
{
	void	*mem;

	if (size == 0)
		return (NULL);
	mem = (void *)malloc(sizeof(*mem) * size);
	if (!mem)
		return (NULL);
	return (ft_memset(mem, 0, size));
}
